  <body>

<p>The findings and interpretation of a pathology laboratory test performed on patient-related specimens.</p>
&#xa;
<p>Comment: This archetype may be used to record a single valued test, but will often be specialised or templated to represent multiple value or &apos;panel&apos; tests. This archetype also acts as the parent for specialisations appropriate for more specific laboratory tests, e.g. microbiology, histopathology.</p>
